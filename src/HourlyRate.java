public class HourlyRate {

    private int price;
    private String time;

    HourlyRate() {

    }

    public int getPrice() {
        return price;
    }
    public String getTime() {
        return time;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
